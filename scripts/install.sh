#!/usr/bin/env bash
# ============================================================
# CCEE Install Script
# Usage:
#   ./scripts/install.sh
#   ./scripts/install.sh --scaffold-obsidian
#   ./scripts/install.sh --cron
#   ./scripts/install.sh --systemd
#   ./scripts/install.sh --scaffold-obsidian --cron --systemd
# ============================================================

set -e

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
PROJECT_DIR="$(cd "$SCRIPT_DIR/.." && pwd)"
USER_HOME="$HOME"

SCAFFOLD_OBSIDIAN=false
INSTALL_CRON=false
INSTALL_SYSTEMD=false

# Parse flags
for arg in "$@"; do
  case "$arg" in
    --scaffold-obsidian) SCAFFOLD_OBSIDIAN=true ;;
    --cron) INSTALL_CRON=true ;;
    --systemd) INSTALL_SYSTEMD=true ;;
  esac
done

echo "=== CCEE Installer ==="
echo "Project: $PROJECT_DIR"
echo ""

# ---- 1. Check system deps -------------------------------------------
echo "Checking system dependencies..."

check_cmd() {
  if command -v "$1" &>/dev/null; then
    echo "  [OK] $1 ($(command -v "$1"))"
  else
    echo "  [MISS] $1 — not found"
    return 1
  fi
  return 0
}

DEPS_OK=true
check_cmd node || DEPS_OK=false
check_cmd npm || DEPS_OK=false
check_cmd git || DEPS_OK=false

# yt-dlp
if [ -f "$USER_HOME/.local/bin/yt-dlp" ]; then
  echo "  [OK] yt-dlp ($USER_HOME/.local/bin/yt-dlp)"
elif command -v yt-dlp &>/dev/null; then
  echo "  [OK] yt-dlp ($(command -v yt-dlp))"
else
  echo "  [MISS] yt-dlp — install with: pip install yt-dlp --break-system-packages"
  DEPS_OK=false
fi

# ffmpeg
if command -v ffmpeg &>/dev/null; then
  echo "  [OK] ffmpeg"
else
  echo "  [MISS] ffmpeg — installing..."
  sudo apt-get install -y ffmpeg 2>/dev/null || echo "  [WARN] ffmpeg install failed (no sudo?)"
fi

# whisper
if python3 -c "import whisper" &>/dev/null 2>&1; then
  echo "  [OK] whisper (Python module)"
elif command -v whisper &>/dev/null; then
  echo "  [OK] whisper (CLI)"
else
  echo "  [MISS] whisper — installing..."
  pip install openai-whisper --break-system-packages 2>/dev/null || \
    echo "  [WARN] whisper install failed — transcription fallback won't work"
fi

echo ""

# ---- 2. npm install -------------------------------------------------
echo "Installing npm dependencies..."
cd "$PROJECT_DIR"
npm install
echo "  [OK] npm install complete"
echo ""

# ---- 3. Create runtime dirs -----------------------------------------
echo "Creating CCEE runtime directories..."
RUNTIME_DIRS=(
  "$USER_HOME/.ccee/runs"
  "$USER_HOME/.ccee/backups"
  "$USER_HOME/.ccee/repo-cache"
  "$USER_HOME/.ccee/test-env"
)
for dir in "${RUNTIME_DIRS[@]}"; do
  mkdir -p "$dir"
  echo "  [OK] $dir"
done
echo ""

# ---- 4. Scaffold Obsidian dirs (optional) ---------------------------
if [ "$SCAFFOLD_OBSIDIAN" = true ]; then
  echo "Scaffolding Obsidian staging directory..."
  STAGING="$USER_HOME/ccee-obsidian-staging"

  DIRS=(
    "$STAGING/CCEE/Architecture"
    "$STAGING/CCEE/Weekly-Reviews"
    "$STAGING/CCEE/User-Guide"
    "$STAGING/CCEE/Repo-Intelligence"
    "$STAGING/CCEE/YouTube-Intelligence/archive"
    "$STAGING/Templates/CCEE"
    "$STAGING/Prompts/VPS/_queue"
  )

  for dir in "${DIRS[@]}"; do
    mkdir -p "$dir"
    echo "  [OK] $dir"
  done

  # Create placeholder files
  cat > "$STAGING/CCEE/_index.md" << 'EOF'
---
updated: auto
---
# CCEE Dashboard

> Auto-updated by CCEE each week. See the Obsidian dashboard template for Dataview queries.
EOF

  cat > "$STAGING/CCEE/Architecture/current-setup.md" << 'EOF'
---
updated: auto
ccee_run: pending-first-run
---
# Current Claude Code VPS Configuration

> Updated automatically after each CCEE run.
EOF

  cat > "$STAGING/CCEE/Repo-Intelligence/tracked-repos.md" << 'EOF'
---
type: ccee-config
---
# Tracked Repositories

## Mandatory (always scanned)
- github.com/gsd-build/gsd-2
- github.com/gsd-build/get-shit-done
- github.com/bmad-code-org/BMAD-METHOD
- github.com/aj-geddes/claude-code-bmad-skills
- github.com/anthropics/claude-skills
- github.com/hesreallyhim/awesome-claude-code

## Operator-added
> Add repos below. One per line, URL only. CCEE picks them up on next run.
EOF

  cat > "$STAGING/CCEE/Repo-Intelligence/tracked-channels.md" << 'EOF'
---
type: ccee-config
---
# Tracked YouTube Channels

## Active Channels
> Add channels below. One per line, @handle. CCEE picks them up on next run.

- @anthropic
- @aicodingclub
- @indydevdan
- @allaboutai
EOF

  cat > "$STAGING/CCEE/YouTube-Intelligence/_weekly-digest.md" << 'EOF'
---
week: pending-first-run
---
# YouTube Intelligence — Latest Digest

> Auto-generated by CCEE. Updated weekly.
EOF

  cat > "$STAGING/CCEE/Repo-Intelligence/gsd.md" << 'EOF'
# GSD Repository Intelligence

- **Repo:** github.com/gsd-build/gsd-2
- **Last scanned:** pending first run
- **Patterns tracked:** context engineering, sub-agent orchestration, wave-based parallelism
EOF

  cat > "$STAGING/CCEE/Repo-Intelligence/bmad.md" << 'EOF'
# BMAD Repository Intelligence

- **Repo:** github.com/bmad-code-org/BMAD-METHOD
- **Last scanned:** pending first run
- **Patterns tracked:** agile AI workflow, expansion packs, scale-adaptive intelligence
EOF

  cat > "$STAGING/CCEE/Repo-Intelligence/discoveries.md" << 'EOF'
# Repo Discoveries

> New repositories discovered by CCEE during research sweeps.
> Auto-populated weekly.
EOF

  # User Guide stubs
  for page in getting-started invoking-ccee review-ui-guide mission-control-integration troubleshooting configuration; do
    cat > "$STAGING/CCEE/User-Guide/${page}.md" << EOF
# ${page//-/ }

> Documentation stub — will be populated after first CCEE run.
EOF
  done

  # Templates
  cp "$PROJECT_DIR/src/obsidian/templates/weekly-review.md" \
    "$STAGING/Templates/CCEE/weekly-review.md" 2>/dev/null || true
  cp "$PROJECT_DIR/src/obsidian/templates/index-dashboard.md" \
    "$STAGING/Templates/CCEE/index-dashboard.md" 2>/dev/null || true

  cat > "$STAGING/Templates/CCEE/repo-tracker.md" << 'EOF'
---
type: repo-intelligence
repo_url:
last_scanned:
head_sha:
---
# {{repo_name}} Intelligence

## Patterns Adopted

## Notable Changes

## Assessment
EOF

  cat > "$STAGING/Templates/CCEE/channel-tracker.md" << 'EOF'
---
type: channel-intelligence
channel:
url:
last_checked:
---
# {{channel}} Intelligence

## Recent Videos

## Key Takeaways

## Applicable Config Changes
EOF

  echo "  [OK] Obsidian staging scaffold complete at $STAGING"
  echo ""
fi

# ---- 5. Cron (optional) ---------------------------------------------
if [ "$INSTALL_CRON" = true ]; then
  echo "Installing cron entry..."
  CRON_CMD="0 0 * * 0 $PROJECT_DIR/scripts/run-weekly.sh >> $USER_HOME/.ccee/runs/cron.log 2>&1"

  # Add if not already present
  if crontab -l 2>/dev/null | grep -qF "run-weekly.sh"; then
    echo "  [SKIP] cron entry already exists"
  else
    (crontab -l 2>/dev/null; echo "$CRON_CMD") | crontab -
    echo "  [OK] cron entry added: $CRON_CMD"
  fi
  echo ""
fi

# ---- 6. Systemd (optional) ------------------------------------------
if [ "$INSTALL_SYSTEMD" = true ]; then
  echo "Installing systemd service..."
  SERVICE_FILE="$PROJECT_DIR/scripts/ccee-review.service"

  if [ -f "$SERVICE_FILE" ]; then
    if sudo cp "$SERVICE_FILE" /etc/systemd/system/ccee-review.service 2>/dev/null; then
      sudo systemctl daemon-reload
      sudo systemctl enable ccee-review.service
      sudo systemctl start ccee-review.service
      echo "  [OK] ccee-review.service installed and started"
    else
      echo "  [WARN] Could not install to /etc/systemd/system/ (no sudo)"
      echo "         Manually copy: sudo cp $SERVICE_FILE /etc/systemd/system/"
      echo "         Then: sudo systemctl daemon-reload && sudo systemctl enable --now ccee-review.service"
    fi
  else
    echo "  [MISS] $SERVICE_FILE not found"
  fi
  echo ""
fi

echo "=== CCEE install complete ==="
echo ""
echo "Next steps:"
echo "  1. Copy .env.example to .env and fill in ANTHROPIC_API_KEY and GITHUB_TOKEN"
echo "  2. Run: npm run ccee:run  (first full pipeline run)"
echo "  3. Open: http://localhost:9898 (Review UI)"
