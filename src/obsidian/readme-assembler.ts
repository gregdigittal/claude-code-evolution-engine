/**
 * readme-assembler.ts — Assemble repo README.md from Obsidian User Guide notes.
 *
 * The User Guide folder is structured to be directly exportable as docs/.
 * This module concatenates the relevant notes into a GitHub-ready README.
 */

import { existsSync, readFileSync, writeFileSync } from 'node:fs';
import { join } from 'node:path';
import { createTaggedLogger } from '../utils/logger.js';

const log = createTaggedLogger('readme-assembler');

const USER_GUIDE_SECTIONS = [
  'getting-started.md',
  'invoking-ccee.md',
  'review-ui-guide.md',
  'configuration.md',
] as const;

/**
 * Assemble README.md from User Guide notes and write to repo root.
 */
export function assembleReadme(
  stagingPath: string,
  repoRootPath: string
): void {
  const userGuidePath = join(stagingPath, 'CCEE', 'User-Guide');
  const sections: string[] = [
    '<!-- Auto-generated by CCEE from Obsidian User Guide. Do not edit manually. -->',
    '',
    '# Claude Code Evolution Engine (CCEE)',
    '',
    '> A self-contained research-and-upgrade pipeline that keeps a Claude Code VPS installation at the cutting edge.',
    '',
  ];

  for (const section of USER_GUIDE_SECTIONS) {
    const sectionPath = join(userGuidePath, section);
    if (!existsSync(sectionPath)) {
      log.debug(`User Guide section not found: ${section}`);
      continue;
    }

    try {
      let content = readFileSync(sectionPath, 'utf-8');
      // Strip Obsidian-specific frontmatter
      content = content.replace(/^---[\s\S]*?---\n/, '');
      // Convert wikilinks to markdown links
      content = content.replace(/\[\[([^\]]+)\]\]/g, '[$1]($1.md)');
      sections.push(content.trim());
      sections.push('');
    } catch (err: unknown) {
      log.warn(`failed to read ${section}: ${err instanceof Error ? err.message : String(err)}`);
    }
  }

  // Add architecture diagram reference
  sections.push(
    '## Architecture',
    '',
    '![Current Setup](docs/diagrams/current-setup.svg)',
    '',
    '---',
    '',
    '*Documentation generated by [CCEE](https://github.com/gregdigittal/claude-code-evolution-engine)*',
    ''
  );

  const readmePath = join(repoRootPath, 'README.md');
  writeFileSync(readmePath, sections.join('\n'), 'utf-8');
  log.info(`README.md assembled from ${USER_GUIDE_SECTIONS.length} sections`);
}
